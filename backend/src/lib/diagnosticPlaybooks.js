/**
 * @param {{ symptoms?: string|null, problemConditions?: string|null }} ext
 * @param {string} lastUserText
 */
export function pickPlaybook(ext, lastUserText = '') {
  const text = `${ext?.symptoms || ''} ${ext?.problemConditions || ''} ${lastUserText || ''}`;
  let best = null;
  for (const pb of PLAYBOOKS) {
    let score = 0;
    for (const k of pb.keywords) if (norm(text).includes(k)) score++;
    if (!best || score > best.score) best = { score, pb };
  }
  if (!best || best.score <= 0) return null;
  return best.pb;
}

export function playbookToAiHints(pb) {
  if (!pb) return null;
  return {
    playbookId: pb.id,
    categoryId:
      pb.id === 'engine-misfire' || pb.id === 'engine-no-start' || pb.id === 'engine-check-engine'
        ? 'engine'
        : pb.id === 'cooling-overheat'
          ? 'cooling'
          : pb.id === 'brakes-vibration'
            ? 'brakes'
            : pb.id === 'suspension-knock'
              ? 'suspension'
              : pb.id === 'at-shift'
                ? 'transmission'
                : 'other',
    title: pb.title,
    recommendedService: pb.recommendedService,
    hypotheses: pb.hypotheses,
    checks: pb.checks,
    urgency: pb.urgency,
  };
}

